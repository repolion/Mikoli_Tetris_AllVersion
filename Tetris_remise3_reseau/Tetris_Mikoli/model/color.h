#ifndef COLOR_H
#define COLOR_H

namespace mikoli{

/*!
 * \brief The Color enum
 * This enumeration is used to set a color to a block.
 * Each block has one color and all the blocks from one figure
 * have the same color.
 */
enum class Color
{
    RED,GREEN,YELLOW,BLUE,PURPLE,DEEPPINK,CYAN,ORANGE,GREY
};

}

#endif // COLOR_H
